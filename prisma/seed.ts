async function main() {
  console.log('Seeding database...')

  for (const lessonData of lessons) {
    const { exercises, questions, phrases, ...lessonFields } = lessonData

    const lesson = await prisma.lesson.upsert({
      where: { number: lessonFields.number },
      update: {},
      create: {
        ...lessonFields,
        phrases: phrases as object[],
      },
    })

    console.log(`Created lesson ${lesson.number}: ${lesson.titleRu}`)

    // Delete existing exercises/quiz for this lesson (for re-seeding)
    await prisma.exercise.deleteMany({ where: { lessonId: lesson.id } })
    const existingQuiz = await prisma.quiz.findUnique({ where: { lessonId: lesson.id } })
    if (existingQuiz) {
      await prisma.question.deleteMany({ where: { quizId: existingQuiz.id } })
      await prisma.quiz.delete({ where: { id: existingQuiz.id } })
    }

    // Create exercises
    for (const ex of exercises) {
      await prisma.exercise.create({
        data: {
          lessonId: lesson.id,
          type: ex.type,
          prompt: ex.prompt,
          options: ex.options as object,
          answer: ex.answer,
        },
      })
    }

    // Create quiz with questions
    const quiz = await prisma.quiz.create({
      data: { lessonId: lesson.id },
    })

    for (const q of questions) {
      await prisma.question.create({
        data: {
          quizId: quiz.id,
          textKz: q.textKz,
          textRu: q.textRu,
          options: q.options,
          correctAnswer: q.correctAnswer,
        },
      })
    }

    console.log(`  → ${exercises.length} exercises, ${questions.length} questions`)
  }

  console.log('✅ Database seeded successfully!')
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
